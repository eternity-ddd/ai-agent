"""제주도 종합 안내 PDF 생성 스크립트"""
from fpdf import FPDF
import os

class JejuPDF(FPDF):
    def __init__(self):
        super().__init__()
        font_path = os.path.join(os.path.dirname(__file__), "NanumGothic.ttf")
        self.add_font("NanumGothic", "", font_path)
        self.add_font("NanumGothic", "B", font_path)

    def header(self):
        self.set_font("NanumGothic", "B", 10)
        self.cell(0, 8, "제주도 종합 안내", align="C", new_x="LMARGIN", new_y="NEXT")
        self.ln(2)

    def add_title(self, title):
        self.set_font("NanumGothic", "B", 16)
        self.cell(0, 12, title, new_x="LMARGIN", new_y="NEXT")
        self.ln(4)

    def add_section(self, title):
        self.set_font("NanumGothic", "B", 13)
        self.cell(0, 10, title, new_x="LMARGIN", new_y="NEXT")
        self.ln(2)

    def add_subsection(self, title):
        self.set_font("NanumGothic", "B", 11)
        self.cell(0, 8, title, new_x="LMARGIN", new_y="NEXT")
        self.ln(1)

    def add_body(self, text):
        self.set_font("NanumGothic", "", 10)
        self.multi_cell(0, 6, text)
        self.ln(2)


